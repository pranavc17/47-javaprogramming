

public class Average {
	public static void main(String[] args) {
		
		System.out.println("Sum and Average of 5 numbers:");
		int num1=18;
		int num2=52;
		int num3=66;
		int num4=72;
		int num5=32;
		
		float sum=num1+num2+num3+num4+num5;
		float avg=sum/5;
		
		System.out.println("Sum="+sum);
		System.out.println("Average="+avg);
	}

}
